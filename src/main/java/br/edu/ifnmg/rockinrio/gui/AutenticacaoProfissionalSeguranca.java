    private String getCpfTextFieldConteudo() {
        String cpf = cpfTextField.getText();
        
        boolean isNumeric = cpf.chars().allMatch(Character::isDigit);
        
        if (!isNumeric || cpf.length() != 11) return null;
        
        return cpf;
    }

