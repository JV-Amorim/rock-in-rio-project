    private void submitButtonMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseReleased
        String cpf = getCpfTextFieldConteudo();
        
        if (cpf == null) {
            var mensagemErroDialog = new MensagemErroDialog(this, "O CPF é inválido.");
            mensagemErroDialog.setLocationRelativeTo(this);
            mensagemErroDialog.setVisible(true);
            return;
        }
        
        ProfissionalSeguranca profissionalSeg = ProfissionalSegurancaDao.obter(cpf);
        
        if (profissionalSeg == null) {
            var mensagemErroDialog = 
                new MensagemErroDialog(
                    this, 
                    "Não há registro de profissional de \nsegurança que possua o CPF fornecido."
                );
            mensagemErroDialog.setLocationRelativeTo(this);
            mensagemErroDialog.setVisible(true);
            return;
        }
        
        paginaInicial.abrirGerenciamentoOcorrencias(profissionalSeg.getCpf(), profissionalSeg.getNome());
        dispose();
    }//GEN-LAST:event_submitButtonMouseReleased

